

////////////////////////////////////////////////////////////////////////


#ifndef  __VX_EMPTY_STRING_H__
#define  __VX_EMPTY_STRING_H__


////////////////////////////////////////////////////////////////////////


inline bool    empty (const char * s)  { return ( !s || !(*s) ); }

inline bool nonempty (const char * s)  { return ( s && (*s) ); }


////////////////////////////////////////////////////////////////////////


#endif   /*  __VX_EMPTY_STRING_H__  */


