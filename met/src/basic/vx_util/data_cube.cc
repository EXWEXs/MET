////////////////////////////////////////////////////////////////////////

double DataCube::get(int i, int j, int k) const {

    int n = Ny * Nz * i + Nz * j + k;
    return Data[n];
}

////////////////////////////////////////////////////////////////////////

void DataCube::init_from_scratch() {

    Nx = 0;
    Ny = 0;
    Nz = 0;
    Nxyz = 0;

    clear();
}

////////////////////////////////////////////////////////////////////////

void DataCube::assign(const DataCube& d) {

    clear();

    set_size(d.nx(), d.ny(), d.nz());

    Data = d.Data;

    return;
}

