DataCube::DataCube(const DataCube& d) {

    init_from_scratch();

    assign(d);
}

////////////////////////////////////////////////////////////////////////

DataCube::~DataCube() {

    clear();
}

////////////////////////////////////////////////////////////////////////

DataCube& DataCube::operator=(const DataCube& d) {

    if(this == &d) return(*this);

    assign(d);

    return(*this);
}

