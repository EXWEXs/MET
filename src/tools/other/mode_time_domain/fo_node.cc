////////////////////////////////////////////////////////////////////////


FO_Node::FO_Node()

{

init_from_scratch();

}


////////////////////////////////////////////////////////////////////////


FO_Node::~FO_Node()

{

clear();

}


////////////////////////////////////////////////////////////////////////


FO_Node::FO_Node(const FO_Node & n)

{

init_from_scratch();

assign(n);

}


////////////////////////////////////////////////////////////////////////


FO_Node & FO_Node::operator=(const FO_Node & n)

{

if ( this == &n )  return ( * this );

assign(n);

return ( * this );

}


////////////////////////////////////////////////////////////////////////


void FO_Node::init_from_scratch()

{

clear();

}


////////////////////////////////////////////////////////////////////////


void FO_Node::clear()

{

Number    = 0;

IsFcst    = false;

IsVisited = false;

return;

}


////////////////////////////////////////////////////////////////////////


void FO_Node::assign(const FO_Node & n)

{

clear();


Number = n.Number;

IsFcst = n.IsFcst;

IsVisited = n.IsVisited;


return;

}


////////////////////////////////////////////////////////////////////////


void FO_Node::set_number(int k)

{

Number = k;

return;

}


////////////////////////////////////////////////////////////////////////


void FO_Node::set_visited(bool tf)

{

IsVisited = tf;

return;

}


